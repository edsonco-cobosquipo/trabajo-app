	Pikillacta es considerada en la actualidad como una de las ciudades pre incas más conocidas y mejor conservadas que existen en el Perú. Se encuentraa 30 Km. al sudeste de la ciudad del Cusco, sobre los 3350 metros de altura y cubre un territorio de aproximadamente 50 Has.
	En la época pre inka la Cuenca de Lucre fue ocupado por los Wari, logrando dominar de esta manera la zona, manteniendo supremacía desde Chelques-Paruro. Al respecto, nos indica Ravines (1982) que los Wari antes de asentarse en Pikillaqta, ocuparon primigeniamente el sector de Chelques.Al establecerse en la Cuenca de Lucre los Wari, su organización expansionista se limitó a acciones encaminadas al desarrollo social, económico, político, administrativo, concordante con el desarrollo de las formaciones culturales y locales.La importancia de los Wari radicó en la planificación urbana y gubernamental alcanzado de esta manera el alto grado de conocimiento en ingeniería, logrando lebantar obras arquitectónicas de gran envergadura.
</p>


