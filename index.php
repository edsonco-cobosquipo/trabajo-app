	<p class="bold" id="prueva4" >
Písac (en quechua P'isaq) está en el distrito homónimo de la provincia de Calca,1 está ubicado a 30 kilómetros de la ciudad del Cusco,2 en el Perú. Su yacimiento arqueológico es uno de los más importantes y visitados del Valle Sagrado de los Incas. Se encuentra al este de la cordillera de Vilcabamba. Como era costumbre en la arquitectura inca, las ciudades fueron construidas sobre la base de trazos figurativos de animales. Písac arqueológico asume la forma de la perdiz de puna (Nohoprocta ornata branicti Taczanovski),3 como expresa su toponimia o denominación originaria.
Písac está divido en dos zonas, la antigua, ubicada en la parte alta, es un sitio arqueológico incaico, y la actual, ubicada en el valle, que data de la época colonial. Su plaza principal es un lugar entretenido lleno de colorido y con diversos artículos artesanales a la venta. Este pueblo es conocido por su observatorio astronómico.
La arquitectura de Písac también es mestiza construida sobre restos indígenas por el virrey Toledo.
</p>


