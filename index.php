	<p class="bold" id="prueva" >El Coricancha (en quechua: Quri Kancha, ‘templo dorado’)?, originalmente Inti Kancha («Templo del sol») es el templo inca sobre el cual fue construido el Convento de Santo Domingo.Fue uno de los más famosos, venerados y respetados templos de la ciudad del Cuzco, en el Perú.
Ruinas actuales del templo del Sol o Coricancha, conservadas dentro del templo de Santo Domingo.
El recinto de oro; como era conocido, era un lugar sagrado donde se rendía adoración al máximo dios inca: el Inti (Sol), por lo que sólo podían entrar en ayunas, descalzos y con una carga en la espalda en señal de humildad, según lo indicaba el sacerdote mayor Willaq Umu .
El frontis era un hermoso muro proveniente de la más fina cantería, decorado únicamente por una fina lámina de oro puro de una palma de alto, a tres metros del suelo, y un techo de paja fina y delicadamente cortada.
En uno de los bloques de la segunda hilada se observan tres agujeros que pudieron ser utilizados para evacuar las aguas de las lluvias del patio interior, o como salida de la chicha que se ofrecía como ofrenda. 
