		<p class="bold" id="prueva2" >
Ollantaytambo (quechua: Ollantay Tampu) es un poblado y sitio arqueológico incaico, capital del distrito de Ollantaytambo(provincia de Urubamba), situado al sur del Perú, a unos 90 km al noroeste de la ciudad del Cuzco.
Durante el incanato, Pachacútec conquistó la región y construyó el pueblo y un centro ceremonial. En la época de la conquistasirvió como fuerte de Manco Inca Yupanqui, líder de la resistencia inca. Es la única ciudad del incanato en el Perú que aún es habitada. En Ollantaytambo hay andenes de resistencia (para evitar deslizamientos), no agrícolas como en los demás sitios arqueológicos del Cuzco. En la actualidad es una importante atracción turística debido a sus construcciones incas y por ser uno de los puntos de partida más comunes del camino inca hacia Machu Picchu.

</p>


	</div>
	
	<p class="bold" id="prueva" >Sacsayhuamán (en quechua Saqsaywaman, de saqsay, lugar de saciarse, y waman, halcón, es decir, "Lugar donde se sacia el halcón") es una "fortaleza ceremonial" inca ubicada dos kilómetros al norte de la ciudad del Cuzco. Se comenzó a construir durante el gobierno de Pachacútec, en el siglo XV; sin embargo, fue Huayna Cápac quien le dio el toque final en el siglo XVI. Con el aniquilamiento de la nobleza inca desaparecieron de la memoria humana las técnicas que permitieron la construcción de esta monumental fortaleza o santuario; el cual produjo la admiración de Pizarro y sus hombres. Pedro Sancho, secretario de Pizarro, deja una primera descripción del edificio; pero la más detallada es la del Inca Garcilaso de la Vega (Cronista mestizo). La "fortaleza ceremonial" de Sacsayhuamán es con sus muros megalíticos, la mayor obra arquitectónica que realizaron los incas durante su apogeo. Desde la fortaleza se observa una singular vista panorámica de los entornos, incluyendo la ciudad del Cuzco.
</p>


